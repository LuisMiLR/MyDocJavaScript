// numbers

/* Séparateur numérique ( _)
Vous pouvez représenter plus clairement des nombres plus grands, 
si vous le souhaitez, avec le séparateur numérique ( _). Par exemple: */

let nb = 1_000_000; // 1000000 (1 million)

//Converting from number to string
/* Bien que rarement utilisé, vous pouvez convertir un nombre en chaîne en appelant la .toString()méthode . */

let answer = 42;
answer.toString(); //"42"
